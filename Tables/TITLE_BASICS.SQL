CREATE TABLE IMDB_STAGE.TITLE_BASICS (
  TCONST VARCHAR2(10) CONSTRAINT TITLE_BASICS_PK PRIMARY KEY
    USING INDEX TABLESPACE USERS,
  TITLE_TYPE VARCHAR2(12),
  PRIMARY_TITLE VARCHAR2(500),
  ORIGINAL_TITLE VARCHAR2(500),
  IS_ADULT NUMBER(1),
  START_YEAR NUMBER(4),
  END_YEAR NUMBER(4),
  RUNTIME_MINUTES NUMBER(6),
  GENRES VARCHAR2(100))
  TABLESPACE USERS
;
