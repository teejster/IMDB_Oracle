CREATE OR REPLACE PACKAGE BODY IMDB_STAGE.PKG_TITLE_CREW AS
  PROCEDURE SP_TRUNCATE_TITLE_CREW IS
  BEGIN
    EXECUTE IMMEDIATE 'TRUNCATE TABLE IMDB_STAGE.TITLE_CREW';
  END SP_TRUNCATE_TITLE_CREW;
  
  PROCEDURE SP_INSERT_TITLE_CREW IS
  BEGIN
  
    INSERT /*+ APPEND */ INTO IMDB_STAGE.TITLE_CREW (
        TCONST,
        DIRECTORS,
        WRITERS)
      SELECT TCONST,
             DIRECTORS,
             WRITERS
        FROM IMDB_STAGE.TITLE_CREW_EXT NBE
    ;

  END SP_INSERT_TITLE_CREW;
  
  PROCEDURE SP_LOAD_TITLE_CREW IS
  BEGIN
    SP_TRUNCATE_TITLE_CREW;
    SP_INSERT_TITLE_CREW;
  END SP_LOAD_TITLE_CREW;
  
END PKG_TITLE_CREW;
