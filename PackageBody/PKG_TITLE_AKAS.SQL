CREATE OR REPLACE PACKAGE BODY IMDB_STAGE.PKG_TITLE_AKAS AS
  PROCEDURE SP_TRUNCATE_TITLE_AKAS IS
  BEGIN
    EXECUTE IMMEDIATE 'TRUNCATE TABLE IMDB_STAGE.TITLE_AKAS';
  END SP_TRUNCATE_TITLE_AKAS;
  
  PROCEDURE SP_INSERT_TITLE_AKAS IS
  BEGIN
  
    INSERT /*+ APPEND */ INTO IMDB_STAGE.TITLE_AKAS (
        TITLE_ID,
        ORDERING,
        TITLE,
        REGION,
        LANGUAGE,
        TYPES,
        ATTRIBUTES,
        IS_ORIGINAL_TITLE)
      SELECT TAE.TITLE_ID,
             TAE.ORDERING,
             TAE.TITLE,
             TAE.REGION,
             TAE.LANGUAGE,
             TAE.TYPES,
             TAE.ATTRIBUTES,
             TAE.IS_ORIGINAL_TITLE
        FROM IMDB_STAGE.TITLE_AKAS_EXT TAE
    ;
  
  END SP_INSERT_TITLE_AKAS;
  
  PROCEDURE SP_LOAD_TITLE_AKAS IS
  BEGIN
    SP_TRUNCATE_TITLE_AKAS;
    SP_INSERT_TITLE_AKAS;
  END SP_LOAD_TITLE_AKAS;
  
END PKG_TITLE_AKAS;
/
SHOW ERR
